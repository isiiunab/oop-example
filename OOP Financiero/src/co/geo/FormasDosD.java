package co.geo;

public class FormasDosD {
	
	private double ancho;
	private double altura;
	
	
	public String toString() {
		return "el ancho es: "
				+ this.ancho
			    + " la altura es: "
				+ this.altura;
	}
	
	public double getAncho() {
		return this.ancho;
	}
	
	public double getAltura() {
		return this.altura;
	}
}
